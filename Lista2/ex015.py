dia = int(input('Digite um número de 1 a 7'))

if(dia == 1):
    print('Segunda')
elif dia == 2:
    print('Terça')
elif dia == 3: 
    print('Quarta')
elif dia == 4:
    print('Quinta')
elif dia == 5:
    print('Sexta')
elif dia == 6:
    print('Sábado')
elif dia == 7:
    print('Domingo')
else:
    print('Dia da semana inválido')